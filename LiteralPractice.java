public class LiteralPractice {
    public static void main(String[] args) {
        
        long l = 1234567890123456789L;
        float preciseFloat = 3.14159f;
        char Symbol = '\u00A9';

        System.out.println("Value of largeNumber (long): " + l);
        System.out.println("Value of preciseFloat (float): " + preciseFloat);
        System.out.println("Value of Copyright Symbol (char): " + Symbol);
    }
}